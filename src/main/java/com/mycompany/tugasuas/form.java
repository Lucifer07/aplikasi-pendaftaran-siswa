    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        try {
            Binsert.setEnabled(false);
            Bupdate.setEnabled(true);
            Bdelete.setEnabled(true);
            int jumlah = jTable1.getSelectedRow();
            if(jumlah == -1){
                return;
            }
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String id = (String)model.getValueAt(jumlah,0);
            NoDaf.setText(id);
            String nama = (String)model.getValueAt(jumlah,1);
            fnama.setText(nama);
            String tempatlahir = (String)model.getValueAt(jumlah,2);
            ftempat.setText(tempatlahir);
            java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(jumlah,3));
            tanggal.setDate(date);
            String jnskelamin = (String)model.getValueAt(jumlah,4);
            if(jnskelamin.equals("L")){
                laki.setSelected(true);
            }
            else if(jnskelamin.equals("P")){
                perempuan.setSelected(true);
            }
            String alamat = (String)model.getValueAt(jumlah,5);
            falamat.setText(alamat);
            String asalsekolah = (String)model.getValueAt(jumlah,6);
            fsekolah.setText(asalsekolah);
            String nilaiuan = (String)model.getValueAt(jumlah,7);
            fnilai.setText(nilaiuan);
            String namaortu = (String)model.getValueAt(jumlah,8);
            fwali.setText(namaortu);
            String notelp = (String)model.getValueAt(jumlah,9);
            fhp.setText(notelp);
        } catch (ParseException ex) {
            Logger.getLogger(form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked
