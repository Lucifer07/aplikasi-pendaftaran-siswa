public void buatId(){
       
        
        try {
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("uuMMdd");
            LocalDate localDate = LocalDate.now();
            String id_pendaftaran = dtf.format(localDate);
            Connection c = con.getcon();
            Statement stmt2 = c.createStatement();
            ResultSet sets2 = stmt2.executeQuery("SELECT COUNT(no_pendaftaran)+1 from pendaftaran_siswa");
            if(sets2.next()){
                fNilai = Integer.parseInt(sets2.getString(1));
              
            }
            String nilaibaru = String.format("%04d", fNilai);
            String idbaru = id_pendaftaran+ nilaibaru;
            NoDaf.setText(idbaru);
                
           
           
        } catch (SQLException ex) {
            Logger.getLogger(form.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }
